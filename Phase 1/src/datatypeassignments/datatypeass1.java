package datatypeassignments;

public class datatypeass1 {

	public static void main(String[] args) {
		
		
		System.out.println("This is my datatype program");
		
		int age =74;
		System.out.println("integer value = "+age);
		
		long l = age;
		System.out.println("long value = "+l);
		
		float f = 79.56f;
		System.out.println("float value = "+f);
		
		boolean status = true;
		System.out.println("boolean status = "+status);
		
		char gender = 'm';
		System.out.println("gender value = "+gender);
		
		long distance = 5672948492716L;
		System.out.println("distance value = "+distance);
		
		int k = (int) distance;
		System.out.println("explicit typecasting value = "+k);
		
		double d = 4556.6738;
		System.out.println("double value = "+d);
		
		
		
		
		System.out.println("integer value = "+age);
		

	}

}
